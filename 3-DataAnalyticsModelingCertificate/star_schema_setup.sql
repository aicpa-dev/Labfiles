DROP TABLE dim_store;
DROP TABLE dim_product;
DROP TABLE dim_time;
DROP TABLE dim_employee;

CREATE TABLE dim_store (
    store_id INT PRIMARY KEY,
    street_address VARCHAR(128) NOT NULL,
    city VARCHAR(45) NOT NULL,
    state VARCHAR(2) NOT NULL,
    country VARCHAR(45) NOT NULL,
    region VARCHAR(24) NOT NULL
);

CREATE TABLE dim_product (
    product_id INT PRIMARY KEY,
    upc VARCHAR(12) NOT NULL UNIQUE,
    name VARCHAR(128) NOT NULL,
    category VARCHAR(24) NOT NULL
);

CREATE TABLE dim_time (
    time_id INT PRIMARY KEY,
    date_d DATE NOT NULL,
    year_num INT NOT NULL,
    quarter_of_year INT NOT NULL,
    week_of_quarter INT NOT NULL,
    day_of_week INT NOT NULL
);

CREATE TABLE dim_employee (
    employee_id int PRIMARY KEY,
    first_name VARCHAR(45) NOT NULL,
    last_name VARCHAR(45) NOT NULL
);

CREATE TABLE fact_sale (
    time_id INT NOT NULL,
    store_id INT NOT NULL,
    product_id INT NOT NULL,
    employee_id INT NOT NULL,
    price DECIMAL(6,2),
    quantity INT,
    PRIMARY KEY (time_id, store_id, product_id, employee_id),
    FOREIGN KEY (time_id) REFERENCES dim_time(time_id),
    FOREIGN KEY (store_id) REFERENCES dim_store(store_id),
    FOREIGN KEY (product_id) REFERENCES dim_product(product_id),
    FOREIGN KEY (employee_id) REFERENCES dim_employee(employee_id)
);

CREATE TABLE fact_inventory_order (
    time_id INT NOT NULL,
    store_id INT NOT NULL,
    product_id INT NOT NULL,
    quantity INT,
    PRIMARY KEY (time_id, store_id, product_id),
    FOREIGN KEY (time_id) REFERENCES dim_time(time_id),
    FOREIGN KEY (store_id) REFERENCES dim_store(store_id),
    FOREIGN KEY (product_id) REFERENCES dim_product(product_id)
);

